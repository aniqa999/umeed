const getFilteredDisasters = async (req, res) => {
  try {
    const { fromDate, toDate, region, disasterType, severity, status } =
      req.query;

    const query = {};

    if (fromDate || toDate) {
      query.startDate = {};
      if (fromDate) query.startDate.$gte = new Date(fromDate);
      if (toDate) query.startDate.$lte = new Date(toDate);
    }

    if (region && region !== "all") {
      query.province = new RegExp(`^${region}$`, "i");
    }

    if (disasterType && disasterType !== "all") {
      query.disasterType = disasterType;
    }

    if (status && status !== "all") {
      const statusMap = {
        active: "Ongoing",
        resolved: "Terminated",
        monitoring: "Contained",
      };
      query.status = statusMap[status] || status;
    }

    if (severity && severity !== "all") {
      query.severity =
        severity.charAt(0).toUpperCase() + severity.slice(1).toLowerCase();
    }

    const disasters = await Disaster.find(query).sort({ startDate: -1 });

    res.json({
      success: true,
      count: disasters.length,
      data: disasters,
    });
  } catch (error) {
    res.status(500).json({ success: false, message: error.message });
  }
};

const getMonthlyStats = async (req, res) => {
  try {
    const start = new Date();
    start.setDate(1);
    start.setHours(0, 0, 0, 0);

    const end = new Date();

    const disasters = await Disaster.find({
      startDate: { $gte: start, $lte: end },
    });

    let totalAffected = 0;
    const regionStats = {};
    const severityBreakdown = { Low: 0, Medium: 0, High: 0 };
    const typeBreakdown = {};

    disasters.forEach((d) => {
      const affected = d.impact?.affected_population || 0;
      totalAffected += affected;

      if (!regionStats[d.province]) regionStats[d.province] = 0;
      regionStats[d.province] += affected;

      const sev = d.severity || calculateSeverity(d.impact);
      if (severityBreakdown[sev] !== undefined) severityBreakdown[sev]++;

      if (!typeBreakdown[d.disasterType]) typeBreakdown[d.disasterType] = 0;
      typeBreakdown[d.disasterType]++;
    });

    res.json({
      success: true,
      totalAffected,
      regionStats,
      severityBreakdown,
      typeBreakdown,
      totalDisasters: disasters.length,
    });
  } catch (error) {
    res.status(500).json({ success: false, message: error.message });
  }
};

const updateDisaster = async (req, res) => {
  try {
    const { duration_days, ...body } = req.body;

    const disaster = await Disaster.findById(req.params.id);
    if (!disaster) {
      return res
        .status(404)
        .json({ success: false, message: "Disaster not found." });
    }

    const allowedFields = [
      "status",
      "endDate",
      "impact",
      "technicalData",
      "description",
    ];
    const updates = {};

    allowedFields.forEach((field) => {
      if (body[field] !== undefined) updates[field] = body[field];
    });

    let impactChanged = false;
    if (updates.impact) {
      updates.impact = { ...disaster.impact.toObject(), ...updates.impact };
      updates.severity = calculateSeverity(updates.impact);
      impactChanged = true;
    }

    const updated = await Disaster.findByIdAndUpdate(req.params.id, updates, {
      new: true,
      runValidators: true,
    });

    let resourceRecalculationNeeded = false;
    if (impactChanged) {
      resourceRecalculationNeeded = true;

      await ActivityLog.log({
        userId: req.user._id,
        userRole: req.user.role,
        category: "disaster",
        action: "IMPACT_UPDATED",
        target: { model: "DisasterEvent", id: updated._id },
        description: `Impact updated for ${updated.disasterType} in ${updated.province}. NGOs should recalculate resources if needed.`,
        req,
        statusCode: 200,
        success: true,
        metadata: {
          disasterId: updated._id,
          impactChanged: true,
          resourceRecalculationNeeded: true,
        },
      });
    }

    await ActivityLog.log({
      userId: req.user._id,
      userRole: req.user.role,
      category: "disaster",
      action: "UPDATED",
      target: { model: "DisasterEvent", id: updated._id },
      description: `Updated ${updated.disasterType} disaster in ${updated.province}`,
      req,
      statusCode: 200,
      success: true,
      metadata: {
        updatedFields: Object.keys(updates),
        resourcesNeedRecalculation: impactChanged,
      },
    });

    res.json({
      success: true,
      disaster: updated,
      message: impactChanged
        ? "Disaster updated. Resources may need recalculation by NGOs."
        : "Disaster updated successfully.",
    });
  } catch (error) {
    res.status(500).json({ success: false, message: error.message });
  }
};

