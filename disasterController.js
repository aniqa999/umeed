const ML_API_URL = process.env.ML_API_URL || "http://localhost:8000";

const predictAndSave = async (req, res) => {
  try {
    const {
      title,
      description,
      startDate,
      duration_days = 7,
      ...mlFeatures
    } = req.body;

    const mlResponse = await fetch(`${ML_API_URL}/predict`, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify(mlFeatures),
    });

    if (!mlResponse.ok) {
      const errBody = await mlResponse.json().catch(() => ({}));
      return res.status(502).json({
        success: false,
        message: "ML service error",
        detail: errBody.detail || mlResponse.statusText,
      });
    }

    const { predictions } = await mlResponse.json();

    const typeMap = {
      Flood: "Flood",
      Earthquake: "Earthquake",
      Drought: "Drought",
      Wildfire: "Wildfire",
      Cyclone: "Cyclone",
      Heatwave: "Heatwave",
    };
    const disasterType = typeMap[mlFeatures.disaster_type] || "Other";

    const autoTitle =
      title ||
      `${disasterType} — ${mlFeatures.province} (${new Date().toLocaleDateString("en-PK")})`;

    const payload = {
      title: autoTitle,
      description: description || `AI-predicted impact for ${autoTitle}.`,
      disasterType,
      status: "Ongoing",
      startDate: startDate ? new Date(startDate) : new Date(),
      country: "Pakistan",
      province: mlFeatures.province,
      location: {
        type: "Point",
        coordinates: [
          Number(mlFeatures.longitude),
          Number(mlFeatures.latitude),
        ],
      },
      impact: {
        deaths: Math.round(predictions.deaths),
        injured: Math.round(predictions.injured),
        affected_population: Math.round(predictions.affected_population),
        houses_damaged: Math.round(predictions.houses_damaged),
        houses_demolished: Math.round(predictions.houses_demolished),
        crop_area_damaged: Math.round(predictions.crop_area_damaged),
      },
      technicalData: {
        magnitude: mlFeatures.magnitude || undefined,
        river_discharge_cusecs: mlFeatures.river_discharge_cusecs || undefined,
        temperature_max: mlFeatures.temperature_max || undefined,
      },
    };

    payload.createdBy = req.user._id;

    payload.severity = calculateSeverity(payload.impact);

    const disaster = new Disaster(payload);
    const saved = await disaster.save();

    await ActivityLog.log({
      userId: req.user._id,
      userRole: req.user.role,
      category: "disaster",
      action: "PREDICTED",
      target: { model: "DisasterEvent", id: saved._id },
      description: `AI prediction created for ${saved.disasterType} in ${saved.province} — severity: ${saved.severity}`,
      req,
      statusCode: 201,
      success: true,
      metadata: {
        disasterType: saved.disasterType,
        province: saved.province,
        severity: saved.severity,
        mlPredictions: predictions,
        resourcesAutoGenerated: false,
      },
    });

    return res.status(201).json({
      success: true,
      predictions,
      disaster: saved,
      message: "Disaster predicted and saved. Resources can be added by NGOs.",
    });
  } catch (error) {
    console.error("[predictAndSave]", error);
    res.status(500).json({ success: false, message: error.message });
  }
};

const syncResourceRecord = async (disaster, duration = 7) => {
  const calc = calculateResources({
    affected_population: disaster.impact.affected_population,
    injured: disaster.impact.injured,
    houses_damaged: disaster.impact.houses_damaged,
    houses_demolished: disaster.impact.houses_demolished,
    duration_days: duration,
    province: disaster.province,
  });

  return Resource.findOneAndUpdate(
    { disasterId: disaster._id },
    {
      disasterId: disaster._id,
      region: disaster.province,
      affected_population: calc.executive_summary.total_population,
      households: calc.executive_summary.total_households,
      water_liters: calc.detailed_resources.water_liters,
      food_kg: calc.detailed_resources.food_kg,
      food_tons: calc.logistics.total_food_tons,
      shelter: calc.detailed_resources.shelter,
      nfi: calc.detailed_resources.nfi_kits,
      health: calc.detailed_resources.health,
      sanitation: calc.detailed_resources.sanitation,
      logistics: {
        trucks_required: calc.logistics.trucks_required,
        storage_space_sqft: calc.logistics.storage_space_sqft,
      },
    },
    { returnDocument: "after", upsert: true, setDefaultsOnInsert: true },
  );
};

